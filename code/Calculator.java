package code;
import java.io.*;
public class Calculator {

    // method
    public  int add(int a, int b)
    {
        Integer addresult = a + b;
        return addresult;
    }
    public int sub(int a, int b)
    {
        Integer subresult = a - b;
        return subresult;
    }
    public int mul(int a, int b)
    {
        Integer mulresult = a * b;
        return mulresult;
    }
    public float div(float a, float b)
    {
        float divresult = a / b;
        return divresult;
    }
}
